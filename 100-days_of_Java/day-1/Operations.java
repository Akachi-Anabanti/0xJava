public class Operations {
	public static void main(String[] args) {
		// perform addition
		int result = 2 + 4;
		System.out.println("Addition of 2 and 4=" + result);
		
		// perform substraction
		result = 4 - 2;
		System.out.println("Substraction of 2 from 4=" + result);
		
		// perform multiplication
		result = 2 * 2;
		System.out.println("mulitplication of 2 by 2=" + result);
		
		// perform bitwise or
		byte b_result = 2 | 3;
		System.out.println("Bitwise 'or' of 2 and 3=" + b_result);
		
		// perform bitwise and
		b_result = 2 & 3;
		System.out.println("Bitwise 'and' of 2 and 3=" + b_result);
		
		// perform bitwise XOR
		b_result = 2 ^ 3;
		System.out.println("Bitwise 'XOR' on 2 and 3=" + b_result);
	}
}
